%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Printing
%%%%%%%%%%%%%%%%%%%%%%%%%%%%

print() ->
    [FirstDict | Rest] = 
        [   to_dict(L) 
        ||  L <- [results_2011(), results_2012(), results_2013(), results_2014(), results_2015(), results_2016()]],
    JointDict = 
        lists:foldl(
            fun join_dict/2,
            FirstDict,
            Rest),
    print_latex(JointDict).


join_dict(DictA, DictB) ->
    dict:fold(
        fun(K, DA, Dict) ->
            case dict:fetch(K, DictB) of 
                DB ->
                    dict:store(K, join_prop_dict(DA, DB), Dict)
            end
        end,
        dict:new(),
        DictA).  

join_prop_dict(DictA, DictB) ->
    dict:fold(
        fun(K, {VA1, VA2}, Dict) ->
            case dict:fetch(K, DictB) of 
                {VB1, VB2} ->
                    dict:store(K, {VA1 + VB1, VA2 + VB2}, Dict)
            end
        end,
        dict:new(),
        DictA).  

to_dict(L) ->
    dict:from_list([{K, dict:from_list(V)} || {K, V} <- L]).

% print_dict(JointDict) ->
%     [io:format( "{~p, ~p},\n", [A, dict:to_list(DL)]) || {A, DL} <- dict:to_list(JointDict)].

print_latex(Dict) ->
    [{_, DictProp} | _] = 
        dict:to_list(Dict),
    Props = 
        dict:fetch_keys(DictProp),
    lists:map(
        fun(P) ->
            Values = 
                dict:fold(
                    fun (K, DictAlg, Acc) ->
                        {V, C} = dict:fetch(P, DictAlg),
                        [{K, {V/C, C}} | Acc]
                    end,
                    [],
                    Dict),
            % io:format("Values: ~p\n", [Values]),
            MaxFun = 
                case lists:member(P, ["size", "time"]) of 
                    true -> 
                        fun lists:min/1;
                    false -> 
                        fun lists:max/1
                end,
            Max = 
                MaxFun([V || {_, {V, _}} <- Values]),
            DictValues = dict:from_list(Values),
            io:format("$~s$", [replace_chars(P)]),
            [begin 
                {V, C} = dict:fetch(A, DictValues),
                case V of 
                    Max -> 
                        io:format(" & \\textbf{~.2f} (~p)", [V, C]);
                    _ ->
                        io:format(" & ~.2f (~p)", [V, C])
                end
             end
            || A <- [   "Llorens et al's slicer", 
                        "Llorens et al's slicer improved", 
                        "Rakow's slicer CTL", 
                        "Rakow's slicer safety", 
                        "Yu et al's slicer"]
            ],
            io:format(" \\\\ \n")
        end,
        Props).

replace_chars([$_|Rest]) -> 
    [$\\,$_ | replace_chars(Rest)];
replace_chars([Other|Rest]) -> 
    [Other | replace_chars(Rest)];
replace_chars([]) -> 
    [].

